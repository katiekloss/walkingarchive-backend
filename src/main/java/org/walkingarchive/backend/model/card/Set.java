package org.walkingarchive.backend.model.card;

public class Set
{
	Integer id;
	String name;

	public Set() {}

	public Integer getId()
	{
		return id;
	}

	public void setId(Integer value)
	{
		id = value;
	}

	public String getName()
	{
		return name;
	}

	public void setName(String value)
	{
		name = value;
	}
}